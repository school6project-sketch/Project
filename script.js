// Функции для обработки лайков/дизлайков
function likeNews(id) {
    const newsItem = newsData.find(item => item.id === id);
    if (newsItem) {
        newsItem.likes += 1;
        renderCurrentPage();
    }
}

function dislikeNews(id) {
    const newsItem = newsData.find(item => item.id === id);
    if (newsItem) {
        newsItem.dislikes += 1;
        renderCurrentPage();
    }
}

// Определяем текущую страницу и рендерим
function renderCurrentPage() {
    const isIndex = document.getElementById('news-grid') !== null;
    const isNewsPage = document.getElementById('news-article') !== null;

    if (isIndex) {
        renderMainPage();
    } else if (isNewsPage) {
        renderNewsPage();
    }
}

// Переменная для хранения текущей выбранной категории (на главной)
let currentCategory = null;

// Рендеринг главной с фильтром по категории
function renderMainPage() {
    const grid = document.getElementById('news-grid');
    const title = document.getElementById('category-title');
    if (!grid) return;

    // Фильтруем новости по категории, если выбрана
    let filteredNews = newsData;
    if (currentCategory) {
        filteredNews = newsData.filter(item => item.category === currentCategory);
        if (title) title.textContent = `Новости категории: ${currentCategory}`;
    } else {
        if (title) title.textContent = 'Последние новости';
    }

    if (filteredNews.length === 0) {
        grid.innerHTML = '<p class="error">В этой категории пока нет новостей</p>';
        return;
    }

    let html = '';
    filteredNews.forEach(item => {
        html += `
            <div class="news-card">
                <img src="${item.image}" alt="${item.title}" onerror="this.src='https://via.placeholder.com/300x200?text=Fake+News'">
                <div class="news-content">
                    <h2>${item.title}</h2>
                    <div class="news-meta">${item.date} · ${item.category}</div>
                    <p class="news-summary">${item.summary}</p>
                    <div class="news-actions">
                        <button class="like-btn" onclick="likeNews(${item.id})">👍 ${item.likes}</button>
                        <button class="dislike-btn" onclick="dislikeNews(${item.id})">👎 ${item.dislikes}</button>
                    </div>
                    <a href="news.html?id=${item.id}" class="read-more">Читать далее</a>
                </div>
            </div>
        `;
    });
    grid.innerHTML = html;
    
    // Подсвечиваем активный пункт меню
    highlightActiveCategory();
}

// Рендеринг страницы новости с комментариями
function renderNewsPage() {
    const params = new URLSearchParams(window.location.search);
    const id = parseInt(params.get('id'));
    const articleContainer = document.getElementById('news-article');

    if (!id) {
        articleContainer.innerHTML = '<p class="error">Новость не указана</p>';
        return;
    }

    const newsItem = newsData.find(item => item.id === id);
    if (!newsItem) {
        articleContainer.innerHTML = '<p class="error">Новость не найдена</p>';
        return;
    }

    // Генерируем блок комментариев
    let commentsHtml = '';
    if (newsItem.comments && newsItem.comments.length > 0) {
        commentsHtml = '<div class="comments-section"><h3>Комментарии</h3>';
        newsItem.comments.forEach(comment => {
            commentsHtml += `
                <div class="comment">
                    <strong>${comment.name}</strong>
                    <p>${comment.text}</p>
                </div>
            `;
        });
        commentsHtml += '</div>';
    }

    const html = `
        <div class="article-header">
            <h1>${newsItem.title}</h1>
            <div class="article-meta">${newsItem.date} · ${newsItem.category}</div>
        </div>
        <img src="${newsItem.image}" alt="${newsItem.title}" class="article-image" onerror="this.src='https://via.placeholder.com/800x400?text=Fake+News'">
        <div class="article-content">
            ${newsItem.content}
        </div>
        <div class="article-actions">
            <button class="like-btn" onclick="likeNews(${newsItem.id})">👍 ${newsItem.likes}</button>
            <button class="dislike-btn" onclick="dislikeNews(${newsItem.id})">👎 ${newsItem.dislikes}</button>
        </div>
        ${commentsHtml}
        <a href="index.html" class="read-more">← На главную</a>
    `;
    articleContainer.innerHTML = html;
}

// Функция для установки фильтра по категории
function setCategory(category) {
    currentCategory = category;
    renderMainPage();
}

// Подсветка активной категории в меню
function highlightActiveCategory() {
    // Убираем выделение у всех пунктов меню с data-category
    document.querySelectorAll('nav a[data-category]').forEach(link => {
        link.style.fontWeight = 'normal';
        link.style.textDecoration = 'none';
    });
    // Если есть активная категория, выделяем соответствующий пункт
    if (currentCategory) {
        const activeLink = document.querySelector(`nav a[data-category="${currentCategory}"]`);
        if (activeLink) {
            activeLink.style.fontWeight = 'bold';
            activeLink.style.textDecoration = 'underline';
        }
    }
    // Снимаем выделение с "Главная" всегда
    const homeLink = document.getElementById('home-link');
    if (homeLink) {
        homeLink.style.fontWeight = 'normal';
        homeLink.style.textDecoration = 'none';
        if (!currentCategory) {
            homeLink.style.fontWeight = 'bold';
            homeLink.style.textDecoration = 'underline';
        }
    }
}

// Инициализация обработчиков на главной странице
function initCategoryFilters() {
    // Обработчики для пунктов меню с категориями
    document.querySelectorAll('nav a[data-category]').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault(); // предотвращаем переход по пустой ссылке
            const category = link.getAttribute('data-category');
            setCategory(category);
        });
    });

    // Обработчик для ссылки "Главная"
    const homeLink = document.getElementById('home-link');
    if (homeLink) {
        homeLink.addEventListener('click', (e) => {
            e.preventDefault(); // предотвращаем перезагрузку страницы
            setCategory(null); // сброс фильтра
        });
    }
}

// Запуск при загрузке страницы
document.addEventListener('DOMContentLoaded', () => {
    // Проверяем, что мы на главной (есть news-grid)
    if (document.getElementById('news-grid')) {
        initCategoryFilters();
    }
    renderCurrentPage();
});